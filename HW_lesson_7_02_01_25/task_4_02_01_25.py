# Letter with days of the week
days_of_week = ["Monday","Tuesday","Wednesday","Thursday","Friday","Saturday","Sunday"]
print(days_of_week)

# Dictionary with days of the week
days_of_week_dict = {1:"Monday",2:"Tuesday",3:"Wednesday",4:"Thursday",5:"Friday",
                     6:"Saturday",7:"Sunday"}
print(days_of_week_dict)

# Reverse dictionary
reversed_days_of_week_dict = {value: key for key, value in days_of_week_dict.items()}
print(reversed_days_of_week_dict)
